create table if not exists public.players (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  skill_level public.skill_level not null default 'Beg',
  current_status public.player_status not null default 'idle',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint players_name_not_blank check (length(trim(name)) > 0)
);

create unique index if not exists players_name_unique_lower
  on public.players (lower(name));

create table if not exists public.courts (
  id integer generated by default as identity primary key,
  court_number integer not null unique,
  status public.court_status not null default 'available',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.queue_entries (
  id uuid primary key default gen_random_uuid(),
  player_id uuid not null references public.players(id) on delete cascade,
  status public.queue_status not null default 'waiting',
  joined_at timestamptz not null default now(),
  assigned_at timestamptz,
  cancelled_at timestamptz
);

create unique index if not exists queue_one_waiting_entry_per_player
  on public.queue_entries (player_id)
  where status = 'waiting';

create index if not exists queue_entries_waiting_order
  on public.queue_entries (joined_at)
  where status = 'waiting';

create table if not exists public.court_assignments (
  id uuid primary key default gen_random_uuid(),
  court_id integer not null references public.courts(id) on delete cascade,
  status public.assignment_status not null default 'playing',
  started_at timestamptz not null default now(),
  ended_at timestamptz
);

create unique index if not exists court_one_active_assignment
  on public.court_assignments (court_id)
  where status = 'playing';

create table if not exists public.assignment_players (
  assignment_id uuid not null references public.court_assignments(id) on delete cascade,
  player_id uuid not null references public.players(id) on delete cascade,
  primary key (assignment_id, player_id)
);

create table if not exists public.activity_log (
  id uuid primary key default gen_random_uuid(),
  message text not null,
  created_at timestamptz not null default now()
);

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

