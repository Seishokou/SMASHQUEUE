-- Book Court screen tables and columns.
-- This section supports the Expo Book tab directly:
-- players_queue, courts with Available/Occupied state, and match_history.

drop trigger if exists courts_assign_first_waiting_player on public.courts;
drop function if exists public.assign_first_waiting_player_to_available_court();

alter table public.courts
  alter column status drop default;

alter table public.courts
  alter column status type text
  using case
    when lower(status::text) in ('available', 'open') then 'Available'
    when lower(status::text) in ('occupied', 'playing') then 'Occupied'
    else 'Available'
  end;

alter table public.courts
  add column if not exists assigned_player_ids uuid[] not null default '{}',
  add column if not exists assigned_player_names text[] not null default '{}',
  add column if not exists match_started_at timestamptz,
  add column if not exists match_duration_minutes integer not null default 20;

update public.courts
set status = case
  when lower(status) in ('available', 'open') then 'Available'
  when lower(status) in ('occupied', 'playing') then 'Occupied'
  else 'Available'
end;

alter table public.courts
  alter column status set default 'Available';

alter table public.courts
  drop constraint if exists courts_status_check;

alter table public.courts
  add constraint courts_status_check
  check (status in ('Available', 'Occupied'));

insert into public.courts (court_number, status)
values
  (1, 'Available'),
  (2, 'Available'),
  (3, 'Available'),
  (4, 'Available')
on conflict (court_number) do nothing;

create table if not exists public.players_queue (
  id uuid primary key default gen_random_uuid(),
  player_name text not null,
  skill_level text not null default 'Beg',
  status text not null default 'waiting',
  court_id integer references public.courts(id) on delete set null,
  match_group_id uuid,
  created_at timestamptz not null default now(),
  assigned_at timestamptz,
  finished_at timestamptz,
  constraint players_queue_name_not_blank check (length(trim(player_name)) > 0),
  constraint players_queue_skill_check check (skill_level in ('Beg', 'Int', 'Adv')),
  constraint players_queue_status_check check (status in ('waiting', 'playing', 'done'))
);

create index if not exists players_queue_waiting_order
  on public.players_queue (created_at)
  where status = 'waiting';

create index if not exists players_queue_status_idx
  on public.players_queue (status);

create table if not exists public.match_history (
  id uuid primary key default gen_random_uuid(),
  court_id integer references public.courts(id) on delete set null,
  court_number integer,
  team_a_players text[] not null default '{}',
  team_b_players text[] not null default '{}',
  team_a_score integer not null,
  team_b_score integer not null,
  winner text not null default 'Draw',
  match_date date not null default current_date,
  match_started_at timestamptz,
  match_ended_at timestamptz,
  played_at timestamptz not null default now(),
  constraint match_history_scores_not_negative check (team_a_score >= 0 and team_b_score >= 0),
  constraint match_history_winner_check check (winner in ('Team A', 'Team B', 'Draw'))
);

alter table public.match_history
  add column if not exists court_number integer,
  add column if not exists winner text not null default 'Draw',
  add column if not exists match_date date not null default current_date,
  add column if not exists match_started_at timestamptz,
  add column if not exists match_ended_at timestamptz;

update public.match_history
set winner = 'Draw'
where winner is null;

update public.match_history
set match_date = played_at::date
where match_date is null;

alter table public.match_history
  alter column winner set default 'Draw',
  alter column winner set not null,
  alter column match_date set default current_date,
  alter column match_date set not null;

alter table public.match_history
  drop constraint if exists match_history_winner_check;

alter table public.match_history
  add constraint match_history_winner_check
  check (winner in ('Team A', 'Team B', 'Draw'));

create index if not exists match_history_played_at_idx
  on public.match_history (played_at desc);

